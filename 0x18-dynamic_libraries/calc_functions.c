# include "cfunctions.h"
int add(int a, int b)
{
	int c = a + b;

	return (c);
}
int sub(int a, int b)
{
	int d = a  - b;

	return (d);
}
int mul(int a, int b)
{
	int e =  a * b;

	return (e);
}
int div(int a, int b)
{
	int f =  a / b;

	return (f);
}
int mod(int a, int b)
{
	int g =  a % b;

	return (g);
}
